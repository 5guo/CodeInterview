#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << pow(2.5, 2) <<endl;
    cout << pow(2.5, 0) <<endl;
    cout << pow(2.5, -2) <<endl;
    cout << pow(0, 2) << endl;
    cout << pow(0, 0) << endl;
    cout << pow(0, -2) <<endl;
    cout << pow(-2.5, 2) <<endl;
    cout << pow(-2.5, 0) <<endl;
    cout << pow(-2.5, -2) <<endl;
    return 0;
}
